    #[test]
    fn test_to_from_vecs() {
        let a_0_0 = Polynomial::<i32, N>::new(vec![1, 2, 3]);
        let a_0_1 = Polynomial::<i32, N>::new(vec![4, 5, 6]);
        let a_1_0 = Polynomial::<i32, N>::new(vec![7, 8, 9]);
        let a_1_1 = Polynomial::<i32, N>::new(vec![10, 11, 12]);

        let a = Mat::<i32, N, 2, 2> {
            polynomials: [
                [a_0_0.clone(), a_0_1.clone()],
                [a_1_0.clone(), a_1_1.clone()],
            ],
        };

        let vecs = Vec::from(a.clone());
        let b = Mat::from(vecs);

        assert_eq!(a.polynomials, b.polynomials);
    }

