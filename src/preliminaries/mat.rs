    fn test_from_fn() {
        let a = Mat::<i32, N, 2, 2>::from_fn(|| Polynomial::<i32, N>::new(vec![1, 2, 3]));

        let expected = vec![
            vec![Polynomial::<i32, N>::new(vec![1, 2, 3]); 2],
            vec![Polynomial::<i32, N>::new(vec![1, 2, 3]); 2],
        ];
        assert_eq!(Vec::from(a), expected);
    }

    #[test]
    fn test_dot() {
        let a_0_0 = Polynomial::<i32, N>::new(vec![1, 2, 3]);
        let a_0_1 = Polynomial::<i32, N>::new(vec![4, 5, 6]);

        // 1x2 matrix
        let a = Mat::<i32, N, 1, 2> {
            polynomials: [[a_0_0.clone(), a_0_1.clone()]],
        };

        let b_0_0 = Polynomial::<i32, N>::new(vec![1, 2]);
        let b_1_0 = Polynomial::<i32, N>::new(vec![3, 4]);

        // 2x1 matrix
        let b = Mat::<i32, N, 2, 1> {
            polynomials: [[b_0_0.clone()], [b_1_0.clone()]],
        };

        let c = a.dot(&b);

        let expected = [[a_0_0.clone() * b_0_0.clone() + a_0_1.clone() * b_1_0.clone()]];
        assert_eq!(c.polynomials, expected);
    }

    #[test]
    fn test_add() {
        let a_0_0 = Polynomial::<i32, N>::new(vec![1, 2, 3]);
        let a_0_1 = Polynomial::<i32, N>::new(vec![4, 5, 6]);

        // 1x2 matrix
        let a = Mat::<i32, N, 1, 2> {
            polynomials: [[a_0_0.clone(), a_0_1.clone()]],
        };

        let b_0_0 = Polynomial::<i32, N>::new(vec![1, 2, 3]);
        let b_0_1 = Polynomial::<i32, N>::new(vec![4, 5, 6]);

        // 1x2 matrix
        let b = Mat::<i32, N, 1, 2> {
            polynomials: [[b_0_0.clone(), b_0_1.clone()]],
        };

        let c = a.add(&b);

        let expected = [[a_0_0.clone() + b_0_0.clone(), a_0_1.clone() + b_0_1.clone()]];
        assert_eq!(c.polynomials, expected);
    }

    #[test]
    fn test_matrix_multiply() {
        const Q: i64 = 12289;
        const N: usize = 4;
        let a = vec![
            vec![
                Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![1, 2, 0, 0; Q]),
                Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![3, 4, 0, 0; Q]),
            ],
            vec![
                Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![5, 6, 0, 0; Q]),
                Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![7, 8, 0, 0; Q]),
            ],
        ];
        let b = vec![
            vec![Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![1, 0, 0, 0; Q])],
            vec![Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![0, 1, 0, 0; Q])],
        ];
        let c = matrix_multiply::<Q, N>(&a, &b);
        assert_eq!(c.len(), a.len());
        assert_eq!(c[0].len(), b[0].len());
        assert_eq!(
            c[0][0],
            Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![1, 2, 0, 0; Q])
                * Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![1, 0, 0, 0; Q])
                + Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![3, 4, 0, 0; Q])
                    * Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![0, 1, 0, 0; Q])
        );
        assert_eq!(
            c[1][0],
            Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![5, 6, 0, 0; Q])
                * Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![1, 0, 0, 0; Q])
                + Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![7, 8, 0, 0; Q])
                    * Polynomial::<ZqI64<Q>, N>::new(zqi64_vec![0, 1, 0, 0; Q])
        );
    }

